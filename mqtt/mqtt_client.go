package mqttclient

import (
	MQTT "github.com/eclipse/paho.mqtt.golang"
)

// Connect to connect to MQTT
func Connect() bool {
	opts := MQTT.NewClientOptions().AddBroker("tcp://iot.eclipse.org:1883")
	opts.SetClientID("go-simple")

	return true
}
